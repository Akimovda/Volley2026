			<script>
				// С эффектом "наезжания" на соседние секции
				const sec1 = document.querySelector('.sec-1');
				const sec2 = document.querySelector('.sec-2');
				const sec3 = document.querySelector('.sec-3');
				
				window.addEventListener('scroll', () => {
					const scrollY = window.scrollY;
					const sec2Top = sec2.offsetTop;
					const windowHeight = window.innerHeight;
					
					// Когда секция 2 появляется в видимости
					if (scrollY > sec2Top - windowHeight) {
						const progress = Math.min(1, (scrollY - (sec2Top - windowHeight)) / windowHeight);
						
						// 1. Сначала sec2 выезжает поверх sec1
						sec2.style.transform = `translateY(${-progress * 50}px)`;
						sec2.style.opacity = progress;
						
						// 2. Затем начинает скроллиться быстрее
						if (progress > 0.5) {
							const fastProgress = (progress - 0.5) * 2; // 0-1
							const fastScroll = fastProgress * 100; // На 100px быстрее
							sec2.style.transform = `translateY(${-50 - fastScroll}px)`;
							
							// 3. Sec3 следует за ней
							sec3.style.transform = `translateY(${-fastScroll * 0.7}px)`;
						}
					}
				});
