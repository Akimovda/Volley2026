<h2 class="-mt-05" id="o1">Как стать организатором</h2>
<p>По умолчанию все пользователи имеют роль «Игрок». Чтобы получить возможность создавать мероприятия — подайте заявку в <a href="{{ route('profile.show') }}">профиле</a> в разделе «Хочу стать организатором».</p>
<p>Укажите в комментарии кратко — что планируете организовывать: регулярные игры, тренировки, турниры. Администратор рассмотрит заявку и активирует роль организатора.</p>

<h2 id="o2">Как создать мероприятие</h2>
<p>После получения роли организатора в меню появится кнопка <strong>«Создать мероприятие»</strong>. Мастер создания состоит из трёх шагов:</p>
<ul class="list">
    <li><strong>Шаг 1 — Тип:</strong> направление (классика / пляж), формат (игра, тренировка, турнир), уровень допуска, тренер</li>
    <li><strong>Шаг 2 — Время и место:</strong> дата, время, площадка, часовой пояс, повторяемость</li>
    <li><strong>Шаг 3 — Детали:</strong> оплата, приватность, описание, обложка, бот-ассистент</li>
</ul>
<p>Мероприятие можно сделать <strong>повторяющимся</strong> — например, каждую пятницу. Система автоматически создаст будущие даты на 90 дней вперёд.</p>

<h2 id="o3">Управление участниками</h2>
<p>На странице мероприятия перейдите в раздел <strong>«Участники»</strong>. Там можно вручную добавить игрока, изменить его позицию, отменить или удалить запись.</p>
<p>Если у вас есть команда сотрудников (staff) — они также могут управлять участниками в рамках ваших мероприятий.</p>

<h2 id="o4">Оплата и транзакции</h2>
<p>Настройте способ приёма оплаты в <a href="{{ route('profile.show') }}">профиле</a> → раздел «Платёжные настройки». Доступные методы: ЮKassa (автоматическое подтверждение), Т-Банк и Сбербанк (подтверждение вручную).</p>
<p>История всех платежей доступна в разделе <strong>«Транзакции»</strong>. Там же можно подтвердить оплату по ссылке или сделать возврат.</p>



<h2 id="o6">Уведомления участникам</h2>
<p>При изменении или отмене мероприятия все участники автоматически получают уведомление в подключённые каналы (Telegram, VK, MAX).</p>
<p>Также работают автоматические напоминания — за настраиваемое время до начала мероприятия.</p>

<h2 id="o7">Рекламные мероприятия</h2>
<p>Рекламное мероприятие — это мероприятие без онлайн-записи через сервис (например, турнир со своей системой регистрации). Оно отображается в общем каталоге как анонс.</p>
<p>Размещение рекламного мероприятия платное. После создания вы получите ссылку на оплату. После подтверждения оплаты мероприятие появится в общем списке.</p>

