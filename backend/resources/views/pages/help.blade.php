<h2 class="-mt-05" id="o1">Как стать организатором</h2>
<p>По умолчанию все пользователи имеют роль «Игрок». Чтобы получить возможность создавать мероприятия — подайте заявку в <a href="{{ route('profile.show') }}">профиле</a> в разделе «Хочу стать организатором».</p>
<p>Укажите в комментарии кратко — что планируете организовывать: регулярные игры, тренировки, турниры, лига. Администратор рассмотрит заявку и активирует роль организатора.</p>

<h2 id="o2">Как создать мероприятие</h2>
<p>После получения роли организатора в меню появится кнопка <strong>«Создать мероприятие»</strong>. Мастер создания состоит из трёх шагов:</p>
<ul class="list">
    <li><strong>Шаг 1 — Тип:</strong> направление (классика / пляж), формат (игра, тренировка, турнир, рекламное), уровень допуска, тренер</li>
    <li><strong>Шаг 2 — Время и место:</strong> дата, время, площадка, часовой пояс, повторяемость, настройки регистрации</li>
    <li><strong>Шаг 3 — Детали:</strong> оплата, приватность, описание, обложка, бот-ассистент</li>
</ul>
<p>Мероприятие можно сделать <strong>повторяющимся</strong> — например, каждую пятницу. Система автоматически создаёт будущие даты на 90 дней вперёд. Каждый тур серии можно редактировать независимо — переопределить локацию, описание, настройки регистрации для конкретной даты.</p>

<h2 id="o3">Управление участниками</h2>
<p>На странице мероприятия перейдите в раздел <strong>«Участники»</strong>. Там можно:</p>
<ul class="list">
    <li>Вручную добавить игрока по имени или нику</li>
    <li>Изменить позицию участника</li>
    <li>Подтвердить или отменить запись</li>
    <li>Экспортировать список участников в PDF или текстовый файл</li>
    <li>Управлять листом ожидания — добавлять игроков вручную, менять порядок очереди</li>
</ul>
<p>Если у вас есть команда сотрудников (staff) — они также могут управлять участниками в рамках ваших мероприятий.</p>

<h2 id="o-waitlist">Лист ожидания</h2>
<p>Лист ожидания на игровых мероприятиях работает <strong>автоматически</strong>: как только участник отменяет запись, система сразу находит следующего подходящего кандидата из очереди и записывает его без дополнительных подтверждений.</p>
<p>Приоритет: <strong>Премиум-игроки</strong> → затем по порядку в очереди (sort_order, затем дата). Как организатор, вы можете изменять порядок очереди вручную — кнопки ↑↓ в разделе «Ожидающие».</p>
<p>Гендерное ограничение учитывается при авто-бронировании: если у мероприятия открыто «окно» для ограниченного пола, участники другого пола не блокируют доступ к свободным местам.</p>
<p>Вы также можете добавить игрока в лист ожидания вручную из панели управления участниками.</p>

<h2 id="o-tournament">Турниры</h2>
<p>Для формата «Турнир» доступна отдельная страница управления — <strong>«Сетка турнира»</strong>. Процесс проведения:</p>
<ul class="list">
    <li><strong>Сбор команд</strong> — игроки регистрируются командами (или вы добавляете вручную). Команды с неполным составом отображаются отдельно («ищут партнёра»)</li>
    <li><strong>Жеребьёвка</strong> — распределите команды по группам вручную или нажмите «Случайная жеребьёвка». Доступны форматы: группы + плей-офф, олимпийская сетка, швейцарская система, круговой турнир, «Король пляжа»</li>
    <li><strong>Матчи</strong> — вносите счёт прямо на странице. Таблица очков, кросс-таблица и турнирная сетка обновляются автоматически</li>
    <li><strong>Тай-брейк</strong> — при равенстве очков система создаёт тай-брейкерный матч или предлагает разрешить ничью жеребьёвкой</li>
</ul>
<p>Ссылка на публичную страницу турнира доступна участникам — там видны группы, матчи и сетка в режиме реального времени.</p>

<h2 id="o-league">Лиги и сезоны</h2>
<p>Для проведения регулярных соревнований (лиги, чемпионаты) используется система <strong>Лиги → Сезон → Дивизионы → Туры</strong>:</p>
<ul class="list">
    <li><strong>Лига</strong> — долгосрочная структура (например, «Любительская Пляжная Лига»). Может включать фидерную лигу для команд с повышением/понижением</li>
    <li><strong>Сезон</strong> — временной период (Весна, Лето, Осень). Внутри сезона — один или несколько дивизионов</li>
    <li><strong>Туры</strong> — отдельные мероприятия (турниры), которые входят в сезон</li>
</ul>
<p><strong>Промоушен</strong> — по итогам сезона система автоматически повышает лучшие команды в вышестоящий дивизион и понижает аутсайдеров. Настраивается в параметрах дивизиона: сколько команд повышается/понижается.</p>
<p>Рейтинг OpenSkill и Elo считается отдельно по каждому сезону — помимо общей карьерной статистики.</p>

<h2 id="o4">Оплата и транзакции</h2>
<p>Настройте способ приёма оплаты в <a href="{{ route('profile.show') }}">профиле</a> → раздел «Платёжные настройки». Доступные методы: ЮKassa (автоматическое подтверждение), Т-Банк и Сбербанк (подтверждение вручную).</p>
<p>История всех платежей доступна в разделе <strong>«Транзакции»</strong>. Там же можно подтвердить оплату по ссылке или сделать возврат.</p>
<p><strong>Виртуальный кошелёк</strong> — участники могут пополнить баланс, и списание за мероприятия будет происходить автоматически. Управление балансом доступно вам как организатору в профиле каждого участника.</p>
