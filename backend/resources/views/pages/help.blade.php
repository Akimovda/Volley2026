<h2 class="-mt-05" id="p1">Регистрация и вход</h2>
<p>На VolleyPlay.Club не нужно придумывать пароль — вход осуществляется через привычные сервисы: <strong>Telegram</strong>, <strong>ВКонтакте</strong>, <strong>Яндекс</strong>, <strong>Google</strong>, а для пользователей iPhone/iPad — <strong>Apple</strong>. Мы не получаем доступ к вашей переписке или личным фотографиям — только базовые данные профиля (имя, аватар).</p>
<p>При первом входе сервис попросит указать имя, город и уровень игры — это нужно для подбора подходящих мероприятий.</p>
<p><strong>Что будет если зайти под другим провайдером?</strong> Если вы ранее входили через Telegram, а теперь попробуете зайти через VK — создастся новый аккаунт. Чтобы этого не происходило, привяжите все аккаунты в настройках профиля.</p>

<h2 id="p2">Привязка дополнительных аккаунтов</h2>
<p><strong>Зачем привязывать?</strong> Привязка позволяет входить на сайт через любой из провайдеров и получать уведомления в разных каналах одновременно — например, и в Telegram, и во ВКонтакте.</p>
<p><strong>Как привязать:</strong> откройте <a href="{{ route('profile.show') }}">Профиль</a> → раздел «Аккаунты и привязки» → нажмите кнопку нужного провайдера и пройдите авторизацию.</p>
<p><strong>Как отвязать:</strong> в том же разделе нажмите «Отвязать» рядом с нужным провайдером. Убедитесь, что хотя бы один аккаунт остаётся привязан — иначе вы не сможете войти.</p>
<p><strong>Доступные провайдеры:</strong> Telegram, ВКонтакте, Яндекс, Google, Apple (только iOS).</p>

<h2 id="p-app">Мобильное приложение</h2>
<p>VolleyPlay.Club доступен в виде нативного приложения для смартфонов. Скачайте его из магазина приложений вашего устройства:</p>
<ul class="list">
    <li><strong>iPhone / iPad:</strong> App Store → «VolleyPlay»</li>
    <li><strong>Android:</strong> RuStore → «VolleyPlay»</li>
</ul>
<p>Приложение даёт ряд преимуществ по сравнению с браузером:</p>
<ul class="list">
    <li><strong>Push-уведомления</strong> — мгновенные оповещения о записи, изменениях и напоминаниях прямо на экран телефона, без мессенджеров</li>
    <li><strong>Вход по Face ID / Touch ID</strong> — после первого входа можно авторизоваться биометрией, без нажатия кнопок</li>
    <li><strong>Быстрый доступ</strong> — иконка на рабочем столе, работает как обычное приложение</li>
</ul>
<p>Все функции сайта полностью доступны в приложении. Аккаунт у вас один — данные синхронизируются между сайтом и приложением автоматически.</p>

<h2 id="p3">Как записаться на мероприятие</h2>
<p>Перейдите на страницу <a href="{{ route('events.index') }}">мероприятий</a>, выберите подходящее и нажмите кнопку <strong>«Записаться»</strong>. Для некоторых мероприятий нужно выбрать позицию (связующий, доигровщик и т.д.).</p>
<p>Если все места заняты — можно встать в <strong>лист ожидания</strong>. При освобождении места система автоматически запишет вас — уведомление придёт в подключённый мессенджер или через push.</p>
<p>Приватные мероприятия доступны только по ссылке-приглашению от организатора.</p>

<h2 id="p-waitlist">Лист ожидания</h2>
<p>Если на мероприятии нет свободных мест — вы можете встать в <strong>лист ожидания</strong>. Как только кто-то отменит свою запись, система автоматически занесёт вас в состав участников — отдельно подтверждать не нужно.</p>
<p>Приоритет в листе ожидания: сначала обрабатываются <strong>Премиум-игроки</strong>, затем — в порядке очереди (по дате вступления). Организатор может вручную изменить порядок очереди.</p>
<p>Если в мероприятии есть ограничение по полу (например, «только женщины записываются раньше»), система учитывает гендерное окно при выборе следующего участника.</p>
<p>Вы можете покинуть лист ожидания в любой момент — кнопка «Выйти из очереди» на странице мероприятия.</p>

<h2 id="p-team">Турниры и команды</h2>
<p>На турнирах регистрация происходит командами (2×2, 4×4 и другие форматы). Чтобы принять участие:</p>
<ul class="list">
    <li>Создайте команду на странице турнира → укажите название и пригласите партнёров</li>
    <li>Либо примите приглашение от капитана команды — уведомление придёт в мессенджер</li>
    <li>Команда подаёт заявку на участие после набора необходимого числа игроков</li>
</ul>
<p>Вы можете сохранить состав постоянной команды в <a href="{{ route('profile.show') }}">профиле</a> → «Мои команды» — чтобы не собирать их заново на каждом турнире.</p>
<p>На некоторых турнирах действует <strong>индивидуальная регистрация</strong> — игроки записываются по позиции, а организатор формирует команды самостоятельно.</p>

<h2 id="p4">Уведомления</h2>
<p>Сервис отправляет уведомления в подключённые каналы: <strong>Telegram</strong>, <strong>ВКонтакте</strong>, <strong>MAX</strong> и <strong>push-уведомления</strong> в мобильном приложении. Уведомления приходят о записи, отмене, изменениях мероприятия, результатах матчей и напоминаниях перед игрой.</p>
<p>Для получения уведомлений в Telegram — напишите нашему боту и нажмите «Старт». Для ВКонтакте — аналогично через VK-бот. Для push-уведомлений — скачайте мобильное приложение и разрешите уведомления при первом запуске.</p>
<p>Настройки уведомлений (какие типы включить и в какие каналы) — в <a href="{{ route('profile.show') }}">профиле</a>.</p>

<h2 id="p5">Оплата</h2>
<p>Часть мероприятий требует предоплаты. Способ оплаты устанавливает организатор — это может быть ЮKassa (онлайн), Т-Банк или Сбербанк (по ссылке).</p>
<p>После перевода нажмите кнопку <strong>«Я оплатил»</strong> — организатор получит уведомление и подтвердит вашу запись. При оплате через ЮKassa подтверждение происходит автоматически.</p>
<p>Некоторые организаторы используют <strong>абонементы</strong> — при наличии активного абонемента оплата за мероприятие списывается автоматически в момент записи. Управление абонементами доступно в разделе «Мои абонементы» профиля.</p>

<h2 id="p6">Отмена записи</h2>
<p>Отменить запись можно на странице мероприятия — нажмите кнопку <strong>«Отменить запись»</strong>. Организатор может установить время, до которого возможна самостоятельная отмена.</p>
<p>Если время самостоятельной отмены истекло — обратитесь к организатору напрямую.</p>
<p>После вашей отмены система автоматически предложит место первому подходящему участнику из листа ожидания.</p>

<h2 id="p7">Профиль и уровень игры</h2>
<p>В профиле укажите свой уровень игры в классическом и пляжном волейболе — это помогает организаторам настраивать фильтр допуска на мероприятие.</p>
<p>Другие игроки могут оценить ваш уровень — итоговая оценка формируется из среднего значения всех голосов.</p>
<p>Также в профиле можно добавить фото, указать амплуа, предпочтительные зоны (для пляжного), настроить приватность контактных данных и посмотреть историю рейтинга.</p>
<p><strong>Заполненность профиля</strong> влияет на доступ к записи: для некоторых мероприятий обязательны дата рождения, пол или уровень — система попросит дополнить данные при попытке записаться.</p>

<h2 id="p-rating">Рейтинг игроков</h2>
<p>На сервисе действует <strong>рейтинговая система OpenSkill</strong> — алгоритм, аналогичный используемому в онлайн-играх. Он оценивает не только количество побед, но и силу соперников. Рейтинг обновляется автоматически после каждого завершённого матча.</p>
<p><strong>Консервативный рейтинг (CR)</strong> — публичный показатель, отображаемый в таблице. Формула: <code>CR = μ − 3σ</code>, где μ — ожидаемый уровень, σ — неопределённость. Чем больше матчей сыграно, тем точнее рейтинг.</p>
<ul class="list">
    <li>Новый игрок начинает с CR ≈ 0 и растёт по мере участия в турнирах</li>
    <li>Рейтинг считается отдельно для пляжного и классического волейбола</li>
    <li>Помимо OpenSkill, ведётся классический рейтинг <strong>Elo</strong> — по итогам турниров</li>
</ul>
<p>Посмотреть таблицу рейтинга — <a href="{{ route('players.rating') }}">страница рейтинга</a>. В своём профиле доступны история рейтинга, постоянные партнёры и статистика встреч с соперниками. Подробное объяснение алгоритма — на <a href="{{ route('pages.rating_info') }}">странице о рейтинге</a>.</p>

